/**
  * @brief This function handles TIM1 update interrupt and TIM10 global interrupt.
  */
void TIM1_UP_TIM10_IRQHandler(void)
{
  /* USER CODE BEGIN TIM1_UP_TIM10_IRQn 0 */
	score4++;
	// цикл отвечает за считывания времени прыжка
	if (jump_time <= 4 && adc_button) {
		jump_time++;
	} else {
		jump_time = 0;
	}
  /* USER CODE END TIM1_UP_TIM10_IRQn 0 */
  HAL_TIM_IRQHandler(&htim1);
  HAL_TIM_IRQHandler(&htim10);
  /* USER CODE BEGIN TIM1_UP_TIM10_IRQn 1 */

  /* USER CODE END TIM1_UP_TIM10_IRQn 1 */
}

/**
  * @brief This function handles TIM2 global interrupt.
  */
void TIM2_IRQHandler(void)
{
  /* USER CODE BEGIN TIM2_IRQn 0 */
	// считывание значения клавиатуры
	HAL_ADC_Start_IT(&hadc3);
	adc_reg_val = HAL_ADC_GetValue(&hadc3);
	HAL_ADC_Stop_IT(&hadc3);
	adc_button = HAL_GPIO_ReadPin(BUTTON_GPIO_Port, BUTTON_Pin);

	// преобразование уровня сигнала в кнопку управления
	if (3700 <= adc_reg_val && adc_reg_val <= 3800) {
		adc_button = 1;
		poz = 1;
	} else {
		adc_button = 0;
	}
  /* USER CODE END TIM2_IRQn 0 */
  HAL_TIM_IRQHandler(&htim2);
  /* USER CODE BEGIN TIM2_IRQn 1 */

  /* USER CODE END TIM2_IRQn 1 */
}

/**
  * @brief This function handles TIM3 global interrupt.
  */
void TIM3_IRQHandler(void)
{
  /* USER CODE BEGIN TIM3_IRQn 0 */
	// цикл отвечает за время перестановки ног при беге динозаврика
	if (move == 1) {
		move = 2;
	} else {
		move = 1;
	}
  /* USER CODE END TIM3_IRQn 0 */
  HAL_TIM_IRQHandler(&htim3);
  /* USER CODE BEGIN TIM3_IRQn 1 */

  /* USER CODE END TIM3_IRQn 1 */
}

/**
  * @brief This function handles TIM4 global interrupt.
  */
void TIM4_IRQHandler(void)
{
  /* USER CODE BEGIN TIM4_IRQn 0 */
	// цикл отвечает за анимацию мерцания, а именно их количество
	if (beg != 0 && beg <= 28) {
		beg++;
	} else {
		beg = 0;
	}
	score12++;
	// отвечает за переключение положения динозаврика при прыжке
	if (jump_state != 0 && jump_state < (12 - kof)) {
		jump_state++;
	} else {
		jump_state = 0;
		poz = 0;
	}
  /* USER CODE END TIM4_IRQn 0 */
  HAL_TIM_IRQHandler(&htim4);
  /* USER CODE BEGIN TIM4_IRQn 1 */

  /* USER CODE END TIM4_IRQn 1 */
}

/**
  * @brief This function handles TIM8 break interrupt and TIM12 global interrupt.
  */
void TIM8_BRK_TIM12_IRQHandler(void)
{
  /* USER CODE BEGIN TIM8_BRK_TIM12_IRQn 0 */
	// цикл отвечает за счёт игры
	if (game_over == 0) {
		score++;
	}
	// цикл отвечает за время нахождения кактуса на экране
	if (count != 0 && count <= timer + 4) {
	    count++;
    } else {
	    count = 0;
	    mcact = -5;
    }
  /* USER CODE END TIM8_BRK_TIM12_IRQn 0 */
  HAL_TIM_IRQHandler(&htim12);
  /* USER CODE BEGIN TIM8_BRK_TIM12_IRQn 1 */

  /* USER CODE END TIM8_BRK_TIM12_IRQn 1 */
}

